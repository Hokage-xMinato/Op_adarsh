        // Add loading animation on form submit
        document.getElementById('loginForm').addEventListener('submit', function() {
            const btn = document.getElementById('loginBtn');
            const btnText = btn.querySelector('.btn-text');
            const loading = btn.querySelector('.loading');
            
            btnText.classList.add('d-none');
            loading.classList.remove('d-none');
            btn.disabled = true;
        });

        // Add input focus effects
        document.querySelectorAll('.form-control').forEach(input => {
            input.addEventListener('focus', function() {
                this.parentElement.querySelector('.input-icon').style.color = '#667eea';
            });
            
            input.addEventListener('blur', function() {
                this.parentElement.querySelector('.input-icon').style.color = '#718096';
            });
        });

        // Add keyboard shortcut (Enter to submit)
        document.addEventListener('keypress', function(e) {
            if (e.key === 'Enter') {
                document.getElementById('loginForm').submit();
            }
        });

        // Geometric shapes animation enhancement
        document.querySelectorAll('.shape').forEach((shape, index) => {
            shape.style.animationDelay = (index * -5) + 's';
        });
